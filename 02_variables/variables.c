// stdio.h - standard input output library
#include <stdio.h>
int main(void)
{
    // c is a block structured language and block can be defined by these braces
    // inside the block we have sequence of statements
    int n, m; // variable declaration with type
    n = 5;
    m = 8;
    // %d is format specifier for integer value
    printf("The value of n = %d and m = %d \n", n, m);
}